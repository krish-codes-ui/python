# dictionary with string keys
Dict = {'fruit':'mango',
        'vegetable':'cucumber',
        'car':'honda',
        'drink':'fanta',
        'color':'red',
        'number':'67'}
print(Dict)

# Dictionary with the use of integers as keys
Dict2 = {1:'john',
         2:'sam',
         3:'cheeku',
         4:'Krish'}

print(Dict2)

