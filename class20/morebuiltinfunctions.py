#lower
#Returns the exact copy of the strings in lowercase
str1='SAVE EARTH'
print(str1.lower())
#is lower
#Returns true if the string is lowercase
str1='welcome'
print(str1.islower())
#is upper
#Returns true if the string is uppercase
str1='welcome'
print(str1.isupper())
#upper
#Returns the exact copy of the strings in uppercase
str1='welcome'
print(str1.upper())
#title
#returns a string in title case
str1='education department'
print(str1.title())
#swap case
#change the case of every letter and vice versa
str1='tAmIl NaDu'
print(str1.swapcase())
#count
#returns the number of substrings given within the range
str1='Raja Raja Chozhan'
print(str1.count('Raja'))
print(str1.count('r'))
print(str1.count('R'))
#ord(char)
#returns the ASCII code of the character
ch='A'
print(ord(ch))
print(ord('B'))
#chr(ASCII)
#Returns the character represented by a ASCII
ch=97
print(chr(ch))
print(chr(87))
