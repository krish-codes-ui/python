# Tuple with elements

tup1 = (1,2,3,4,5,"Hello","Hi","HAHHA",63,3)
print(tup1)

#tuple without parenthesis

tup2 = 1,2,3,4,5,"Hello","Hi","HAHHA",63,3
print(tup2)

#creating single element tuple

tup3 = (345)   # treated like integer, because no comma
print(type(tup3))

tup3 = (345,)   # treated like tuple, because comma
print(type(tup3))
