# Accesing all elements in a tuple

tup1 = (1,2,3,4,5,"Hello","Hi","HAHHA",63,3)
print(tup1)

# Accesing selected elements using indices

tup1 = (1,2,3,4,5,"Hello","Hi","HAHHA",63,3)
print(tup1[3:8])

# Accesing from the first element up to specified index value

tup1 = (1,2,3,4,5,"Hello","Hi","HAHHA",63,3)
print(tup1[:10])

# Accesing from specified index value to last element

tup1 = (1,2,3,4,5,"Hello","Hi","HAHHA",63,3)
print(tup1[2:])

# Accesing from the first element to the last element

tup1 = (1,2,3,4,5,"Hello","Hi","HAHHA",63,3)
print(tup1[:])
